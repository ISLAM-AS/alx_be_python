## Applying basic Python arithmetic operations & variable assignments to calculate the simple interest on a given principal amount, rate of interest, & time:

# Define three variables: principal, rate, and time:
principal = 1000    # The principal amount (initial investment).
rate = 0.05         # The annual interest rate (as a decimal).
time = 3            # The time the money is invested for, in years.

# Calculate the simple interest
interest = principal * rate * time

# Print the result
print(f"The simple interest is: {interest}")

